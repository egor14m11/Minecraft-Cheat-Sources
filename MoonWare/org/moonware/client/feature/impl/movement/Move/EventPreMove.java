package org.moonware.client.feature.impl.movement.Move;

import org.moonware.client.event.events.Event;

public class EventPreMove implements Event {

    private double x, y, z;

    public EventPreMove(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getZ() {
        return z;
    }

    public void setZ(double z) {
        this.z = z;
    }
}
